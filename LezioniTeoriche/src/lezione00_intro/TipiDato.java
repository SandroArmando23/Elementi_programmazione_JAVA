package lezione00_intro;

public class TipiDato {
	public static void main(String[] args) {
		 // TIPI DI DATO PRIMITIVI
		//Numerici e interi
		byte eta = 33;  //8bit
		byte scarpe = 2;
		byte numStudenti = 127;  // il range è -128 127
		
		int miaEta = 33;
		
		short litri = 30000; //16bit 0 2^16
		
		int soldiInBanca = 2000000; //32bit
		
		long etaTerra = 4000000000000L; //64bit   La L è obbligatoria
		
		//NUmeri con la virgola
		
		float piGreco = 3.14f;    //la f obbligatoria  32bit
		
		double nepero = 2.71;    //64bit
		
		//char,tipo valore caratttere. Ogni lettera corrisponde ad un numero
		char lettera = 'A'; //16bit
		
		//boolean
		boolean vero = true;   //1
		boolean falso = false; //0
		
		
	}

}
